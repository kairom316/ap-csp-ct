function displayValues (amount: number) {
    // repeats the blocks amount times (amount of rounds the user inputted)
    for (let index = 0; index < amount; index++) {
        // randomValue is set a random value from list (image of numbers)
        randomValue = list._pickRandom()
        // mySprite (the blank image sprite) is set to randomValue, which sets it to a random number image from list and is set to a random position on the screen.
        mySprite.setImage(randomValue)
        mySprite.setScale(1, ScaleAnchor.Middle)
        mySprite.setPosition(randint(20, 140), randint(20, 100))
        // finalSum is changed to the sum of the flashing single digit numbers. There is +1 because each number is one less than the position it is in the list, so it takes the index of each number plus one to match the number that was splashed.
        finalSum += list.indexOf(randomValue) + 1
        pause(450)
    }
    // If the user's score is 0, "Goodluck!!!" is splashed
    if (score == 0) {
        game.splash("Goodluck!!!")
    }
}
function checkAnswer (sum: number) {
    // If finalSum equals to sum (the users answer) that the score is increased by one and "Correct!!! :)" is splashed. Then a sound repeats score amount of times. If the finalSum is not equal to the user's answer, "Sorry, that's wrong :(" is splashed.
    if (finalSum == sum) {
        score += 1
        game.splash("Correct!!! :)")
        for (let index = 0; index < score; index++) {
            music.play(music.melodyPlayable(music.baDing), music.PlaybackMode.UntilDone)
        }
    } else {
        game.splash("Sorry that's wrong. :(")
    }
    // If currentRound is not equal to roundAmount (the game still has rounds left), then the users current score and what round it is is splashed.
    if (currentRound != roundAmount) {
        game.splash("Your current score is " + score + "/" + roundAmount)
    }
}
